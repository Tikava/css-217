class TextContent:
    def __init__(self, title, authors, publication_year, abstract, doi, journal, volume, issue, pages, keywords):
        self.title = title
        self.authors = authors
        self.publication_year = publication_year
        self.abstract = abstract
        self.doi = doi
        self.journal = journal
        self.volume = volume
        self.issue = issue
        self.pages = pages
        self.keywords = keywords
